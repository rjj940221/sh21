#include "ft_sh21.h"

void	ft_env(char **split, char **env)
{
	int	i;

	i = 0;
	if (split[1] != NULL)
		return ;
	if (env)
	{
		while (env[i])
		{
			ft_putstr(env[i++]);
			ft_putstr("\n");
		}
	}
}
