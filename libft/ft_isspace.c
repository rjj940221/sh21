#include "libft.h"

int	ft_isspace(int c)
{
	unsigned char	com;

	com = c;
	if (com == '\t' || com == '\n' || com == '\v' ||
			com == '\f' || com == '\r' || com == ' ')
		return (1);
	return (0);
}
