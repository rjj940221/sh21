#include "libft.h"

char	*ft_strncat(char *const s1, const char *const s2, size_t n)
{
	int	i1;
	int	i2;

	i1 = ft_strlen(s1);
	i2 = 0;
	while (i2 < (int)n && s2[i2] != '\0')
	{
		s1[i1] = s2[i2];
		i1++;
		i2++;
	}
	s1[i1] = '\0';
	return (s1);
}
